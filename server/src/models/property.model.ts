const Property = require("mongoose");

const propertySchema = new Property.Schema({
  name: {
    type: String,
    required: true,
  },
  location: {
    type: String,
    required: true,
  },
  type: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  images: { type: String },
});

const Properties = Property.model("Properties", propertySchema);

export { Properties };
